# Operator ตัวดำเนินการ คือ เครื่องหมายที่ใข้ในกานประมวลผลข้อมูล
# Arithmetic Operator เครื่องหมายคำนวณ
# (+ , - , * ,/, % mod , // floor , ** eponential      
print( 10 + 3)
print( 10 - 3)
print( 10 * 3)
print( 10 / 3 )
print( 3 % 10 ) # หารเอาเศษ
print( 3 // 10) # หารเอาส่วน
print( 10 ** 3 )
print('+++++++++++++++++++++++++++++++++++++++++++++') 
# 2. Comparison Operator  เครื่องหมายเปรียบเทียบ (== , != , > , < , >= , <=  )
# ผลัพธ์ที่จะได้มีแค่ True or False
print(10 == 3)
print(10 != 3)
print('Bangna' >= 'Bangpoo')
print('Thailand' < 'THAILAND')
print('++++++++++++++++++++++++++++++++++++++++++')
# 3. Loincal Operator เครื่องหมาย ตรรกะ not , and , or 
# ข้อมูลที่จะใช้กับเครื่องหมายนี้จะต้องเป็น True/False และผลลัพธ์ที่ได่จากการใช้เครื่องหมาย True/False
print(not True)
print(not False)
