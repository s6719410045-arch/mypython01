# if-elif-else

score = 78

if score >= 80: #อย่าลืม colon
    print('Grade A')
elif score >= 70:#อย่าลืม colon
    print('Grade B')
elif score >= 60:#อย่าลืม colon
    print('Grade C')
elif score >= 50:#อย่าลืม colon
    print('Grade D')
else:#อย่าลืม colon *** else สุดท้ายจะเป็น elif ก็ได้
    print('Grade F')

